// This class represents a question with three properties: question, infos, and answer.
// The Question class has a constructor that takes these three properties as required parameters.

// Cette classe représente une question avec trois propriétés : question, infos, et answer.
// La classe Question a un constructeur qui prend ces trois propriétés en tant que paramètres requis.
class Question {
  // Declaration de classe Question
  String question, infos;
  bool answer;

  Question({
    // Constructeur de la Classe
    required this.question,
    required this.infos,
    required this.answer,
  });
}
